# q8_解二元一次方程组
# 这里测试数据为 x+2y = 3
# 这里测试数据为 4x+9y = 7,
a = [[1,2,3],[4,9,7]]

# 1. 消元，消除x
# 1.1 放缩
d = -a[1][0]/a[0][0]
for i in range(len(a[0])):
     a[0][i] = a[0][i]*d
# 1.2加到第二个方程式上去
for i in range(len(a[0])):
    a[1][i] = a[1][i] + a[0][i]
# print(a)
# 2. 打印最终结果 y = a[1][2]/ a[1][1]
# 2.1 首先计算 y
y = a[1][2] /a[1][1]
# 2.2 其次计算x
x = (a[0][2] - a[0][1]*y) / a[0][0]
print('x : '+str(x) + '\ny : '+str(y) )



